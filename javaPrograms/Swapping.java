import java.lang.*;

import java.util.*;

public class Swapping {

	public static void main(String [] args ){

		int a = 10;
		
		int b = 20;
			
		System.out.println("a : " + a);
		System.out.println("b : " + b);

		System.out.println("after Swapping");

		int c = a;
				
		a = b;

		b = c;

		System.out.println("a : " + a);

		System.out.println("b : " + b);


		
	}

}
